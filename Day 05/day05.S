# B=66 (1), F=70 (0), L=76 (0), R=82 (1)

main:
    la    s0,input_start # pointer to current boarding pass
    la    s1,input_end   # pointer to end of boardingn passes
    li    s2,0           # Sum of all IDs
    li    s3,0           # Max ID
    li    s4,-1          # Min ID (unsigned)
    # Iterate through each boarding pass
boarding_pass:
    mv    t0,s0
    #addi  t0,t0,9 # Start at last character in ID string
    mv    t3,x0 # Initialize ID to zero
digit:
    slli  t3,t3,1
    lb    t1,0(t0)
    addi  t2,t1,-70 # is F?
    beqz  t2,digit_done
    addi  t2,t1,-76 # is L?
    beqz  t2,digit_done
    ori   t3,t3,1
digit_done:
    addi  t0,t0,1
    lb    t5,0(t0)
    bnez  t5,digit # Move to next digit, or done with ID
    add   s2,s2,t3
    bleu  t3,s3,not_max # Updating max
    mv    s3,t3
not_max:
    bgeu  t3,s4,not_min # Updating min
    mv    s4,t3
not_min:
    addi  s0,s0,11 # move to next passport
    blt   s0,s1,boarding_pass

    mv    a1,s3
    li    a0,1
    ecall # print max
    mv    a1,s4
    li    a0,1
    ecall # print min
    mv    a1,s2
    li    a0,1
    ecall # print sum of IDs

    # Calculating my seat using n(n+1)/2 trick
    mv    a0,s3
    call  sum_of_first_n_integers
    mv    s5,a0
    addi  a0,s4,-1
    call  sum_of_first_n_integers
    sub   s5,s5,a0
    sub   s5,s5,s2

    mv    a1,s5
    li    a0,1
    ecall # print seat

exit:
    li     a0,10
    ecall

sum_of_first_n_integers:
    addi  t0,a0,1
    mul   t0,t0,a0
    srli  a0,t0,1
    ret

