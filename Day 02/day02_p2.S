    lb    t2,0(t0)
    li    a0,11  # 11 - print_char
    mv    a1,t2
    ecall
    bne   t2,t5,first
    addi  a5,a5,1
first:
    sub   t0,t0,t1
    add   t0,t0,t4
    lb    t2,0(t0)
    li    a0,11  # 4 - print_char
    mv    a1,t2
    ecall
    bne   t2,t5,second
    addi  a5,a5,1
second:

inc_start:
    addi  a5,a5,-1
    bnez  a5,inc_end
    addi  t6,t6,1
    li    a0,1          # 4 - print_str
    mv    a1,t6
    ecall
inc_end:

go_to_next_null:
    addi  t0,t0,1
    lb    t2,0(t0)
    bnez  t2,go_to_next_null
    addi  t0,t0,1


    blt   t0,s3,loop_strings


results:
    # print number of valid passwords
    mv    a1,t6
    li    a0,1
    ecall

    # returning with error code 0
