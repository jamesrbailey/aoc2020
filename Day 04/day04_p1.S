    addi   s7,s7,1
    
field_not_found:
    call   next_passport

    blt    s0,s6,loop_passport

report:
    li     a0,1
    mv     a1,s7
    ecall
exit:
    li     a0,10
    ecall

next_passport:
    lb     t0,0(s0)
    addi   s0,s0,1
    bnez   t0,next_passport
    mv     s1,s0
    ret

print_passport:
    li     a0,4
    mv     a1,s0
    ecall
    ret

# Returns next field entry via a0
next_field:
    lb     t0,0(a0)
    addi   a0,a0,1
ne_space:
    addi   t2,t0,-32
    beqz   t2,ne_return
ne_null:
    bnez   t0,next_field
    addi   a0,a0,-1
ne_return:
    ret

# Moves global field pointer to next field
go_to_next_field:
    addi sp,sp,-32
    sw   ra,0(sp)
    mv   a0,s1
    call next_field
    mv   s1,a0
    lb   a0,0(s1)
    lw   ra,0(sp)
    addi sp,sp,+32
    ret

# Loads field variables in memory
load_field_variables:
    addi sp,sp,-32
    sw   ra,0(sp)
    # Clearing field key variable
    la   a0,key
    li   a2,4
    call mem_clear
    # Clearing field key value
    la   a0,value
    li   a2,32
    call mem_clear
    # Loading field key into "key" variable
    la   a0,key
    mv   a1,s1
    li   a2,3
    call mem_copy
    # Calculating start of field value in a1
    mv   a1,s1
    addi a1,a1,4
    # Finding field value length in a2
    mv   a0,a1
    call next_field
    sub  a2,a0,a1
    # Loading field value into "value" variable
    la   a0,value
    call mem_copy
    lw   ra,0(sp)
    addi sp,sp,32
    ret

# Clears a2 bytes at (a0)
mem_clear:
    sb   x0,0(a0)
    addi a0,a0,1
    addi a2,a2,-1
    bnez a2,mem_clear
    ret

# Copies a2 bytes from (a1) to (a0)
mem_copy:
    lb   t0,0(a1)
    sb   t0,0(a0)
    addi a0,a0,1
    addi a1,a1,1
    addi a2,a2,-1
    bnez a2,mem_copy
    ret

print_field:
    call print_key
    call print_value
    ret

print_key:
    la     a1,key
    li     a0,4
    ecall
    ret

print_value:
    la     a1,value
    li     a0,4
    ecall
    ret

# Searches a passport for the specified field and loads it into memory if found
# Returns pointer to field if found
find_and_load_field:  # a0 is search string and return pointer
    addi sp,sp,-64
    sw   ra,0(sp)
    sw   a0,4(sp)
    mv   s1,s0  # setting field to start of passport
find_loop:
    call load_field_variables
    lw   a0,4(sp)
    la   a1,key
    call str_comp
    beqz a0,found
    call go_to_next_field
    beqz a0,not_found
    j    find_loop
found:
    mv   a0,t0
not_found:
    lw   ra,0(sp)
    addi sp,sp,64
    ret

# String compare function
# Returns numerical difference between strings. ie: zero if matched
str_comp:
    mv   t0,a0
    mv   t1,a1
    mv   a0,x0
comp_char:
    lb   t2,0(t0)
    lb   t3,0(t1)
    add  a0,a0,t2
    sub  a0,a0,t3
    beqz t2,done_comp
    beqz t3,done_comp
    addi t0,t0,1
    addi t1,t1,1
    j    comp_char
done_comp:
    ret



