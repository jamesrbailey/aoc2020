main:
    la    s0,input_start # pointer to current boarding pass
    la    s1,input_end   # pointer to end of boardingn passes
	li    s2,0           # Set for person's answers
	li    s3,0           # Set for group intersection
    li    s4,0           # Sum of all intersected yes's
    li    s5,0           # Set for group union
    li    s6,0           # Sum of all unioned yes's


# Iterate through each group
group:
	mv    t0,s0 # Initializing person to start of group
    li    s3,-1 # Initializing group intersection set
    li    s5,0  # Initializing group union set
person:
	li    s2,0     # Initializing person's answers
answer:
	lb    t1,0(t0) # Loading answer
	beqz  t1,done_person
	addi  t2,t1,-44
	beqz  t2,done_person
	addi  t1,t1,-97 # Adding answer to person's answer field
	li    t3,1
	sll   t3,t3,t1
	or    s2,s2,t3
	addi  t0,t0,1 # Going to next answer
	j     answer
done_person:
	and   s3,s3,s2 # Intersecting this person's answers with rest of group
	or    s5,s5,s2 # Unioning this person's answers with rest of group
	lb    t1,0(t0)
	addi  t0,t0,1
	beqz  t1,done_group
	addi  t2,t1,-44
	beqz  t2,person
done_group:
	# count intersections
	mv    a0,s3
	call count_bits_in_word
	add   s4,s4,a0
	# count unions
	mv    a0,s5
	call count_bits_in_word
	add   s6,s6,a0

	mv    s0,t0
	blt   s0,s1,group

    mv    a1,s4
    li    a0,1
    ecall # print sum of intersections
    mv    a1,s6
    ecall # print sum of unions

exit:
    li     a0,10
    ecall




count_bits_in_word:
	mv    t3,a0
	li    a0,0  # count
bit:
	andi  t4,t3,1
	add   a0,a0,t4
	srli  t3,t3,1
	bnez  t3,bit
	ret



    mul   t0,t0,a0
    srli  a0,t0,1
    ret

