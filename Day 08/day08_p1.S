main:
    la    s0,input_start # pointer to current boarding pass
    la    s1,input_end   # pointer to end of boardingn passes
    li    s2,0           # pc (assuming 1 byte instructions)
    li    s3,0           # acc
    li    s4,0           # op
    li    s5,0           # arg
    li    s6,0           # adjusted address of PC to 8 byte instructions

# initialize visited to zero
    la    a0,visited
    li    a1,1024 # only need to clear 633
    call  mem_clear

run:
    slli  s6,s2,3    # Calculating actual memory address of instruction
    add  s6,s6,s0
    la    t0,visited # checking if instruction visited
    add   t0,t0,s2
    lb    t1,0(t0)
    bnez  t1,done_run
    addi  t1,t1,1
    sb    t1,0(t0)   # incrementing visited count
    lb    s4,0(s6)   # loading operator
    lw    s5,4(s6)   # loading argument
    addi  t0,s4,-97
    bnez  t0,no_add
    add   s3,s3,s5
no_add:
    addi  t0,s4,-106
    bnez  t0,no_jmp
    add   s2,s2,s5
    addi  s2,s2,-1
no_jmp:
    addi  s2,s2,1 # incrementing PC
    j run
done_run:

report:
    li     a0,1
    mv     a1,s3
    ecall
exit:
    li     a0,10
    ecall

# Clears a1 bytes at (a0)
mem_clear:
    sb   x0,0(a0)
    addi a0,a0,1
    addi a1,a1,-1
    bnez a1,mem_clear
    ret

